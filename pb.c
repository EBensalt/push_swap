#include "push_swap.h"

void	pb(t_nood **a, t_nood **b)
{
	t_nood	*ptr;

	if (!(*a))
		return ;
	ptr = *a;
	*a = (*a)->next;
	ptr->next = *b;
	*b = ptr;
	write(1, "pb\n", 3);
}
